"""
backend/report_generator.py
============================
Generates professional PDF medical reports for high-risk AI predictions.

Uses fpdf2 (pure Python, no external binary needed).
Install: pip install fpdf2

Output: reports_output/{user_id}/AarogyaAI_Report_{pred_id}.pdf
"""

